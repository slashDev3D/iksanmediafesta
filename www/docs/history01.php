                        <탑의 나라, 소망을 쌓다>는 지극한 소망은 돌 속에서도 생명을 싹 틔우는 명상의 순간부터 이 땅의 모든 소망이 쌓인 돌탑과 석탑의 나라를 비상하는 초월의 순간, 그리고 서동과 선화로 상징되는 화합의 순간까지 시간과 공간을 초월한 소망의 순간들이 축제에 모인 관객의 삶과 연결되어 있음을 상징한다. 특히 2009년 발굴된 사리장엄구의 기록을 통해 미륵사 창건과 이를 통한 태평성대의 도래를 초현실적인 빛으로 표현하여, 역사-예술-환상이 어우러지는 축제로 관객과 함께 미륵사지를 채운다.<br><br>
